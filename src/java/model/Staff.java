package model;

/**
 *
 * @author Admin
 */
public class Staff {
    private int id;
    private String email;

    public Staff(){}
    
    public Staff(int id, String email) {
        this.id = id;
        this.email = email;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
